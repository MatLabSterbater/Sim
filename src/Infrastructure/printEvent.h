#include "stdio.h"
using namespace std;

//////////////////////////////////////////////////////////////////////////////
//					  	Print to command line								//
//////////////////////////////////////////////////////////////////////////////
void printToTerminal( const char* str )
{
 	std::cout << str;
}

void printToTerminal( const string& str )
{
 	std::cout << str;
}


void printToTerminal( const int str )
{
 	std::cout << str;
}

//////////////////////////////////////////////////////////////////////////////
//							  Print to file									//
//////////////////////////////////////////////////////////////////////////////
void printToFile( FILE* FID, const char* str)
{
 	fprintf(FID,"%s",str);
}
void printToFile( FILE* FID, const string& str)
{
 	fprintf(FID,"%s",str.c_str());
}

void newLine()
{
	cout << "\n";	
}

